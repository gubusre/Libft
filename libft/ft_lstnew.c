#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*elem;

	elem = (t_list *)malloc(sizeof(t_list));
	if (!elem)
		return (NULL);
	elem->content = content;
	elem->next = NULL;
	return (elem);
}
/*
 * Desc
 * 	creates new node using malloc,
 * 	content intialized with content, next init with NULL.
 *
 * Return
 * 	pointer to node or NULL.
 * */
